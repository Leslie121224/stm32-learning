/**
 * @brief 更新 LED 的 PWM 佔空比
 * @param config 指向呼吸燈配置結構體的指標
 */
void Update_LED_Brightness(Breath_LED_t *config) {
    if (config == NULL) return;

    if (config->is_enabled) {
        // 使用 HAL 庫函式修改 Timer 的比較暫存器 (CCR)
        // 這裡暫時用我們結構體裡的數值
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, config->cur_brightness);
    }
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	if(GPIO_Pin == GPIO_PIN_13)
	{
		if(HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13) == GPIO_PIN_RESET)
			LED2.is_enabled = 0;
		else
			LED2.is_enabled = 1;
	}

}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance == USART2)
	{
		if(rx_data == 's') // stop LED breathing
		{
			LED2.is_enabled = 0;

			char stop_msg[50];
			int len = sprintf(stop_msg, "\r\n>>> LED STOP <<<\r\n");
			HAL_UART_Transmit(&huart2, (uint8_t*)stop_msg, len, 100);
		}
		else if(rx_data == 'r')	// resume LED breathing
		{
			LED2.is_enabled = 1;
			char start_msg[50];
			int len = sprintf(start_msg, "\r\n>>> LED START <<<\r\n");
			HAL_UART_Transmit(&huart2, (uint8_t*)start_msg, len, 100);
		}
	}
	HAL_UART_Receive_IT(&huart2, &rx_data, 1);
}
