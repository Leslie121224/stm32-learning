  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_TIM2_Init();
  /* USER CODE BEGIN 2 */
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);

  static uint8_t direction = 0; // 0 for brighter, 1 for darker

  HAL_UART_Receive_IT(&huart2, &rx_data, 1);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */


	  if(LED2.is_enabled)
	  {
		  uint16_t step = 2000 * LOOP_TIME_MS / LED2.period;	// (max brightness / (LED2.period / 2(up to down and down to up) / loop time))
		  if (step == 0) step = 1;	// prevent period too big

		  if(direction == 0)
			  LED2.cur_brightness += step;
		  else
			  LED2.cur_brightness -= step;

		  if(LED2.cur_brightness >= 1000)
		  {
			  LED2.cur_brightness = 1000;
			  direction = 1;
		  }
		  else if (LED2.cur_brightness <= 0)
		  {
			  LED2.cur_brightness = 0;
			  direction = 0;
		  }

		  HAL_Delay(LOOP_TIME_MS);	//ms
		  Update_LED_Brightness(&LED2);

		  char msg_buf[64];
		  // sprintf can transfer my digits to string format, and it'll return how many bytes it cost.
		  //Plus, sprintf will add a \0 at the end, it will occupied a byte in msg_buf, but it won't be counted to the length that sprintf return.
		  int len = sprintf(msg_buf, "Duty Cycle: %d%%\r\n", LED2.cur_brightness);
		  HAL_UART_Transmit(&huart2, (uint8_t*)msg_buf, len, 100);
	  }
  }
